#ifndef __MDNSD_H__
#define __MDNSD_H__

#include <stdint.h>



/*
 * mdnsd initialization structure
 */
struct mdnsd_settings
{
	int priority;
	char *interface;
};


struct rr_entry;

// Initialize mdns
int mdnsd_init(struct mdnsd_settings *mdnsd_settings);

// sets the hostname for the MDNS responder instance
void mdnsd_set_hostname(const char *hostname);

// adds an additional RR
void mdnsd_add_rr(struct rr_entry *rr);

// registers a service with the MDNS responder instance
int mdnsd_register_svc(const char *instance_name,
		const char *type, uint16_t port, const char *txt[]);

// starts a MDNS responder instance
// returns -1 if unsuccessful
int mdnsd_start(void);

// stops the MDNS responder instance
void mdnsd_stop(void);


#endif/*!__MDNSD_H__*/
